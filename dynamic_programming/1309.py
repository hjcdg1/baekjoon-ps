from sys import stdin


N = int(stdin.readline())

# D[i][0] : i번째 행에 사자가 위치하지 않는 경우
# D[i][1] : i번째 행의 첫 번째 칸에 사자 위치하는 경우
# D[i][2] : i번째 행의 두 번째 칸에 사자 위치하는 경우
D = [1, 1, 1]  # 슬라이딩 윈도우 (메모리 절약)

for i in range(2, N + 1):
	D0 = (D[0] + D[1] + D[2]) % 9901
	D1 = (D[0] + D[2]) % 9901
	D2 = (D[0] + D[1]) % 9901
	D[0], D[1], D[2] = D0, D1, D2

print((D[0] + D[1] + D[2]) % 9901)